turbHi		EQU		0x20
turbLo		EQU		0x21
RegA		EQU		0x22
RegB		EQU		0x23
cnt		EQU		0x24
BCDhi		EQU		0x25
BCDlo		EQU		0x26
BCDup		EQU		0x27
temp		EQU		0x28

		ORG		0x00
		goto		main

		ORG		0x10

main
		banksel	PORTB
		bcf		PORTB,RB1
		bcf		PORTB,RB2
		
		banksel	TRISB
		bsf		TRISB,RB2
		bcf		TRISB,RB1

		banksel	PORTB
;		call		WLSensor
		call		turbsens
		call		NTU_reading
		call		bin2bcd

;--------------------------------------------------

WLSensor
		btfsc		PORTB,RB2
		bsf		PORTB,RB1
		bcf		PORTB,RB1
		return

;--------------------------------------------------
;		Turbidity Sensor
;--------------------------------------------------

turbsens
		call		adc_config
		call		dly40

		bsf		ADCON0,GO
;		bcf		ADCON0,ADON

		call		ADConversion

adc_config
		banksel		ADCON1
		movlw		B'10000000'
		movwf		ADCON1

		banksel		ADCON0
		movlw		B'01000101'		;put 01000101 in ADCON0 (channel 1/AN0)	
		movwf		ADCON0
		return		


ADConversion
		btfsc		ADCON0,GO
		goto		ADConversion
		
		bcf		STATUS,RP0
		movf		ADRESH,W		
		movwf 		turbHi		
		bsf 		STATUS,RP0

		movf		ADRESL,W
		bcf		STATUS,RP0
        	movwf		turbLo

			
 
      	       	
dly40
		movlw		D'20'
		movwf		RegA
	
dly		decfsz		RegA,F
		goto		dly
		return


;--------------------------------------------
;		BCD Conversion
;--------------------------------------------
bin2bcd
		call		bcd_config
		call		configBCD
		call		bcd_test
		

bcd_config
		banksel		TRISD
		clrf		TRISD		
		bcf		TRISB,RB5
		bcf		TRISB,RB4
		bcf		TRISB,RB2
		bcf		TRISB,RB1
		
		banksel		PORTD
		return

;-----------------------------------------------------
configBCD				
		
		clrf		BCDlo		;clearing BCD register that will hold the converted number
		clrf		BCDhi		;"		"		"		"	

		movlw		D'16'
		movwf		cnt	
		
		bcf		STATUS,C	; clear carry flag since the rlf instruction affects the carry flag
		goto		convert
		
shift	        
		rlf		temp,F
		rlf		BCDlo,F
		rlf		BCDhi,F
		
		decfsz		cnt,F
		goto		convert
;		goto		extract
		
		
convert
		movlw		0x33
		addwf		BCDlo,F
	
		btfsc		BCDlo,3
		andlw		0xF0
	
		btfsc		BCDlo,7
		andlw		0x0F
	
		subwf		BCDlo,F


		movlw		0x33
		addwf		BCDhi,F
		btfsc		BCDhi,3
		andlw		0xF0
		btfsc		BCDhi,7
		andlw		0x0F
		subwf		BCDhi,F

		rlf		turbLo,F
		rlf		turbHi,F

		rlf		BCDlo,F
		rlf		BCDhi,F
		rlf		BCDup

		decfsz		cnt,F
		goto		convert
		;goto		BCDdigits

BCDdigits
		movf		BCDlo,W
		andlw		0xF0
;		andlw		BCDlo
		movwf		BCDup
		swapf		BCDup
	
		movf		BCDlo,W
		andlw		0x0F
		movwf		BCDlo

		return

