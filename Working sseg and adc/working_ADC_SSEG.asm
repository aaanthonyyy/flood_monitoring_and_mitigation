		call		mux_delay
		bcf		PORTB,RB2

		movlw		0x01
		call		bcd_lkup
		movwf		PORTD
		bsf		PORTB,RB1
		call		mux_delay
		bcf		PORTB,RB1

		goto		bcd_test
		
mux_delay	
		movlw 		0xF9
		movwf 		RegA
		movwf 		RegB
		
dly1		
		decfsz 		RegA
		goto 		dly1
		
dly2		
		decfsz 		RegB
		goto 		dly2
		return	
		
		
sseg_bcd	
		movlw		0x0F
		andwf		ADRESL
		call		bcd_lkup
		bsf		PORTB,RB5
		call		mux_delay
		bcf		PORTB,RB5
	
		movlw		0xF0
		andwf		ADRESL
		call		bcd_lkup
		bsf		PORTB,RB4
		call		mux_delay
		bcf		PORTB,RB4
	
		return
